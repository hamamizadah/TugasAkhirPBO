     public  void tampilkanData(){
    String []kolom = {"ID", "Rating","Judul","Genre","Sutradara","Durasi","Keterangan"};
    ArrayList<Film>list = new Film().getAll();
    Object rowData[] = new Object[7];
    
    tblFilm.setModel(new DefaultTableModel(new Object[][]{}, kolom));
    for (int i = 0; i<list.size(); i++){
        rowData[0]= list.get(i).getIdFilm();
        rowData[1]= list.get(i).getRating().getRatingFilm();
        rowData[2]= list.get(i).getJudul();
        rowData[3]= list.get(i).getGenre();
        rowData[4]= list.get(i).getSutradara();
        rowData[5]= list.get(i).getDurasi();
        rowData[6]= list.get(i).getKeterangan();
        ((DefaultTableModel)tblFilm.getModel()).addRow(rowData);
    }
    }
     public void cari (String keyword){
     String []kolom = {"ID", "Rating","Judul","Genre","Sutradara","Durasi","Keterangan"};
    ArrayList<Film>list = new Film().search(keyword);
    Object rowData[] = new Object[7];
    
    tblFilm.setModel(new DefaultTableModel(new Object[][]{}, kolom));
    for (int i = 0; i<list.size(); i++){
        rowData[0]= list.get(i).getIdFilm();
        rowData[1]= list.get(i).getRating().getRatingFilm();
        rowData[2]= list.get(i).getJudul();
        rowData[3]= list.get(i).getGenre();
        rowData[4]= list.get(i).getSutradara();
        rowData[5]= list.get(i).getDurasi();
        rowData[6]= list.get(i).getKeterangan();
        ((DefaultTableModel)tblFilm.getModel()).addRow(rowData);
    }
     }
     
