    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        Film flm = new Film();
        flm.setIdFilm(Integer.parseInt(txtIdFilm.getText()));
        flm.setRating((Rating)cmbRating.getSelectedItem());
        flm.setJudul(txtJudul.getText());
        flm.setGenre(txtGenre.getText());
        flm.setSutradara(txtSutradara.getText());
        flm.setDurasi(txtDurasi.getText());
        flm.setKeterangan(flm.jadwalTayang((String) cmbJadwalTayang.getSelectedItem()));
        flm.save();
        txtIdFilm.setText(Integer.toString(flm.getIdFilm()));
        
        tampilkanData();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblFilm.getModel();
        int row = tblFilm.getSelectedRow();
        
        Film flm = new Film().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        flm.delete();
        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_btnHapusActionPerformed

    private void tblFilmMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblFilmMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblFilm.getModel();
        int row = tblFilm.getSelectedRow();
        Film flm = new Film().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        txtIdFilm.setText(String.valueOf(flm.getIdFilm()));
        cmbRating.getModel().setSelectedItem(flm.getRating());
        txtJudul.setText(flm.getJudul());
        txtGenre.setText(flm.getGenre());
        txtSutradara.setText(flm.getSutradara());
        txtDurasi.setText(flm.getDurasi());
        cmbJadwalTayang.getModel().setSelectedItem(flm.getKeterangan());
        txtKeterangan.setText(flm.getKeterangan());
    }//GEN-LAST:event_tblFilmMouseClicked

